def get_all_topics() -> list:
    """Return all topics as a flat list."""
    all_topics = []
    for category, topics in HEALTH_TOPICS.items():
        all_topics.extend(topics)
    return all_topics


def get_trending_from_newsdata(count: int = 20) -> list:
    """Fetch trending health news from NewsData.io."""
    if not NEWSDATA_API_KEY:
        return []
    
    try:
        url = "https://newsdata.io/api/1/news"
        params = {
            "apikey": NEWSDATA_API_KEY,
            "q": "health OR ayurveda OR yoga OR nutrition",
            "country": "in",
            "language": "te,en",
            "category": "health",
        }
        resp = requests.get(url, params=params, timeout=15)
        resp.raise_for_status()
        data = resp.json()
        
        topics = []
        for article in data.get("results", [])[:count]:
            title = article.get("title", "")
            if title and len(title) > 10:
                topics.append(title)
        
        print(f"📰 NewsData.io: Found {len(topics)} trending health topics")
        return topics
    
    except Exception as e:
        print(f"⚠️  NewsData.io error: {e}")
        return []


def get_trending_topics(count: int = 10) -> list:
    """
    Get trending health topics.
    Returns 'count' unique topic strings.
    """
    all_topics = []

    # Try NewsData.io first (live trending news)
    news_topics = get_trending_from_newsdata(count)
    if news_topics:
        all_topics.extend(news_topics[:count // 2])

    # Fill from curated list (static topics - always reliable)
    static_topics = get_all_topics()
    random.shuffle(static_topics)
    
    # Add static topics to reach desired count
    needed = count - len(all_topics)
    all_topics.extend(static_topics[:needed * 2])  # Extra buffer for dedup
    
    # Deduplicate and shuffle
    seen = set()
    unique_topics = []
    for t in all_topics:
        lower = t.lower().strip()
        if lower not in seen and len(t) > 5:
            seen.add(lower)
            unique_topics.append(t)
    
    random.shuffle(unique_topics)
    result = unique_topics[:count]
    
    print(f"🎯 Topic discovery complete: {len(result)} topics ready")
    return result


def get_category_topics(category: str, count: int = 5) -> list:
    """Get topics from a specific category."""
    topics = HEALTH_TOPICS.get(category, [])
    random.shuffle(topics)
    return topics[:count]


